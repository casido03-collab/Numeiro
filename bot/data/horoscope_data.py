# ─── Знаки зодиака ─────────────────────────────────────────────────────────────

ZODIAC_SIGNS = [
    # (emoji, name, (month_start, day_start), (month_end, day_end))
    ("♈", "Овен",        (3, 21), (4, 19)),
    ("♉", "Телец",       (4, 20), (5, 20)),
    ("♊", "Близнецы",    (5, 21), (6, 20)),
    ("♋", "Рак",         (6, 21), (7, 22)),
    ("♌", "Лев",         (7, 23), (8, 22)),
    ("♍", "Дева",        (8, 23), (9, 22)),
    ("♎", "Весы",        (9, 23), (10, 22)),
    ("♏", "Скорпион",    (10, 23), (11, 21)),
    ("♐", "Стрелец",     (11, 22), (12, 21)),
    ("♑", "Козерог",     (12, 22), (1, 19)),
    ("♒", "Водолей",     (1, 20), (2, 18)),
    ("♓", "Рыбы",        (2, 19), (3, 20)),
]


def get_zodiac(day: int, month: int) -> tuple[str, str]:
    """Вернуть (emoji, название) знака зодиака по дню и месяцу."""
    for emoji, name, (ms, ds), (me, de) in ZODIAC_SIGNS:
        # Козерог: дек 22 – янв 19 (переход через год)
        if ms > me:
            if (month == ms and day >= ds) or (month == me and day <= de):
                return emoji, name
        else:
            if (month == ms and day >= ds) or (month == me and day <= de) or (ms < month < me):
                return emoji, name
    return "✨", "Неизвестный знак"
