"""Фразы-загрузки перед генерацией AI-ответа.

Хранятся централизованно — все хендлеры используют random_thinking().
"""
import random

THINKING_MESSAGES = [
    "🔮 Анализирую линии судьбы...",
    "✨ Считываю энергетику вашей даты рождения...",
    "🌙 Настраиваюсь на ваш запрос...",
    "💫 Смотрю скрытые узлы вашей ситуации...",
    "🌟 Читаю числа и энергии момента...",
]


def random_thinking() -> str:
    """Случайная фраза-загрузка."""
    return random.choice(THINKING_MESSAGES)
