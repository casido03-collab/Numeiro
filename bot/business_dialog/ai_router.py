PRODUCT_NAMES: dict[str, str] = {
    "love":          "Разбор отношений",
    "money":         "Денежный разбор",
    "energy":        "Энергетический просмотр",
    "future":        "Общий просмотр судьбы",
    "numerology":    "Нумерологический разбор",
    "compatibility": "Разбор совместимости пары",
    "karma":         "Кармический просмотр",
    "general":       "Общий просмотр",
}


def detect_intent(text: str) -> str:
    """Определить тему запроса по тексту. Возвращает ключ из PRODUCT_NAMES."""
    low = text.lower()
    scores: dict[str, int] = {k: 0 for k in _KEYWORDS}
    for intent, keywords in _KEYWORDS.items():
        for kw in keywords:
            if kw in low:
                scores[intent] += 1
    best = max(scores, key=lambda k: scores[k])
    return best if scores[best] > 0 else "general"


def get_product_name(intent: str) -> str:
    return PRODUCT_NAMES.get(intent, PRODUCT_NAMES["general"])
