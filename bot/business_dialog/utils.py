"""Вспомогательные утилиты для business dialog."""
import random
import re

# ─── Приглашение задать уточняющие вопросы после расклада ────────────────────
# {n} — количество оставшихся вопросов, {e} — эмодзи

_FOLLOWUP_INVITES = [
    "Если у вас остались вопросы — вы можете задать ещё {n} {e} Я здесь.",
    "Если что-то хочется уточнить или на душе осталось — у вас есть возможность задать ещё {n} вопроса {e}",
    "Если желание есть — можете обратиться ко мне ещё {n} раза {e} Буду рада.",
    "Если что-то осталось невысказанным — у вас есть ещё {n} вопроса {e} Не торопитесь.",
    "Если захотите уточнить — можете написать ещё {n} раза {e} Я никуда не ухожу.",
    "Если после просмотра что-то важное осталось — у вас есть возможность задать ещё {n} вопроса {e}",
    "Когда будете готовы — можете задать ещё {n} вопроса {e} Я рядом.",
    "Если что-то хочется прояснить — у вас есть ещё {n} вопроса {e} Пишите когда почувствуете нужду.",
    "Если останутся мысли или сомнения — вы можете обратиться ещё {n} раза {e}",
    "У вас есть возможность задать ещё {n} вопроса — если почувствуете что это нужно {e}",
    "Если что-то в вашей ситуации требует уточнения — я готова ответить ещё на {n} вопроса {e}",
    "Если желание поговорить останется — у вас есть ещё {n} вопроса {e} Я слушаю когда будете готовы.",
    "Не обязательно сразу — если захотите что-то уточнить, у вас есть ещё {n} вопроса {e}",
    "Если что-то ещё лежит на сердце — можете написать, у вас есть ещё {n} вопроса {e}",
    "Если после просмотра останется желание поговорить — у вас есть возможность задать ещё {n} вопроса {e}",
]

_ESOTERIC_EMOJIS = ["🌙", "✨", "💫", "🔮", "🌟", "⭐", "🌌", "💎"]


def followup_invite(n: int) -> str:
    """Вернуть случайное приглашение задать уточняющие вопросы."""
    e = random.choice(_ESOTERIC_EMOJIS)
    return random.choice(_FOLLOWUP_INVITES).format(n=n, e=e)


def is_valid_date(text: str) -> bool:
    """Проверить что строка похожа на дату (дд.мм.гггг)."""
    return bool(re.match(r"^\d{1,2}[.\-/]\d{1,2}[.\-/]\d{4}$", text.strip()))


def clean_name(text: str) -> str:
    """Взять первое слово и капитализировать."""
    parts = text.strip().split()
    return parts[0].capitalize() if parts else text
