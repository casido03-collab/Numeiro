"""Вспомогательные утилиты для business dialog."""
import random
import re

# ─── Приглашение задать уточняющие вопросы после расклада ────────────────────
# {n} — количество оставшихся вопросов, {e} — эмодзи

_FOLLOWUP_INVITES = [
    "Душа моя, у вас ещё {n} вопроса — задавайте {e} Я слушаю.",
    "Если что-то осталось на сердце — спрашивайте {e} У вас есть ещё {n} вопроса.",
    "Пишите, не держите в себе {e} Осталось {n} вопроса — я здесь.",
    "Что ещё тревожит, душа моя? У вас {n} вопроса — задавайте {e}",
    "Я жду ваших вопросов {e} Ещё {n} — о чём угодно что важно.",
    "Не уходите с невысказанным {e} Осталось {n} вопроса — спрашивайте.",
    "Что-то ещё лежит на душе? Задайте — у вас есть {n} вопроса {e}",
    "Я рядом, душа моя {e} Если осталось что спросить — {n} вопроса ещё у вас.",
    "{n} вопроса ещё ждут {e} Что хотите прояснить — пишите.",
    "Если ситуация вызывает вопросы — я слушаю. Осталось {n} {e}",
    "Задайте что важно — у вас ещё {n} вопроса {e} Буду отвечать.",
    "Пишите, моя хорошая {e} У вас ещё {n} вопроса — я никуда не ухожу.",
    "Что ещё хочется понять? {n} вопроса — используйте их {e}",
    "Я здесь {e} Если что-то зацепило или осталось непонятным — у вас {n} вопроса.",
    "Не молчите {e} Осталось {n} вопроса — спрашивайте о чём душа просит.",
]

_ESOTERIC_EMOJIS = ["🌙", "✨", "💫", "🔮", "🌟", "⭐", "🌌", "💎"]


def followup_invite(n: int) -> str:
    """Вернуть случайное приглашение задать уточняющие вопросы."""
    e = random.choice(_ESOTERIC_EMOJIS)
    return random.choice(_FOLLOWUP_INVITES).format(n=n, e=e)


def is_valid_date(text: str) -> bool:
    """Проверить что строка похожа на дату (дд.мм.гггг)."""
    return bool(re.match(r"^\d{1,2}[.\-/]\d{1,2}[.\-/]\d{4}$", text.strip()))


def clean_name(text: str) -> str:
    """Взять первое слово и капитализировать."""
    parts = text.strip().split()
    return parts[0].capitalize() if parts else text
