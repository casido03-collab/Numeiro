_DEFAULT_OFFSET = 3  # МСК — fallback


def get_city_tz_offset(city: str) -> int:
    """Вернуть UTC-смещение для города. Fallback — МСК (UTC+3)."""
    key = city.lower().strip()

    # Точное совпадение
    if key in _CITY_OFFSETS:
        return _CITY_OFFSETS[key]

    # Частичное: известный ключ входит в строку или строка входит в ключ
    for known, offset in _CITY_OFFSETS.items():
        if known in key or (len(key) >= 4 and key in known):
            return offset

    return _DEFAULT_OFFSET
